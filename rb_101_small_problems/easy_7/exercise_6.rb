UPPERCASE = ('A'..'Z')
LOWERCASE = ('a'..'z')

def staggered_case(string)
  result = ''
  need_upper = true
  string.chars.each do |char|
    if need_upper
      result += char.upcase
    else
      result += char.downcase
    end
    need_upper = !need_upper if UPPERCASE.include?(char) || LOWERCASE.include?(char) 
  end
  result
end

